import type { MetadataRoute } from "next";
import { SITE_URL } from "@/lib/site";

export default function robots(): MetadataRoute.Robots {
  return {
    rules: {
      userAgent: "*",
      allow: "/",
      disallow: "/api/",
    },
    // TODO: replace with production domain when confirmed
    sitemap: `${SITE_URL}/sitemap.xml`,
  };
}
